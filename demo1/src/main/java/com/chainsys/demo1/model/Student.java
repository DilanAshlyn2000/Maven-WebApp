package com.chainsys.demo1.model;

public class Student {
int id;
int grade;
String name;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getGrade() {
	return grade;
}
public void setGrade(int grade) {
	this.grade = grade;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
}
